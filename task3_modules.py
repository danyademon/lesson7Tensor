def sum(a, b):
    """
    Считает сумму двух чисел
    
    Параметры:
    a -- первое число
    b -- второе число
    Возвращает:
    Сумму а и b
    """
    return a + b

def raz(a, b):
    """
    Считает разность двух чисел
    
    Параметры:
    a -- первое число
    b -- второе число
    Возвращает:
    Разность а и b
    """
    return a - b